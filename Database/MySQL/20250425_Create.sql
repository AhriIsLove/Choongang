    (    DNO       int(2)   PRIMARY KEY,
         DNAME  VARCHAR(20),
         phone    VARCHAR(15),
         POSITION VARCHAR(20)
 ) ;

CREATE TABLE EMP
    (EMPNO int(4)  ,
	 ENAME VARCHAR(10),
 	 JOB   VARCHAR(9),
	 MGR   int(4),
	 HIREDATE DATE,
	 SAL   int(7),
	 COMM  int(7),
	 DEPTNO int(2),
     PRIMARY KEY (EMPNO), 
     FOREIGN KEY (DEPTNO) REFERENCES dept (deptno)
 );
 
